public class DetailLog {
    public String idLog;
    public String idLogUser;
    public String waktu;
    public String isi;

    public String getIdLog() {
        return idLog;
    }

    public void setIdLog(String idLog) {
        this.idLog = idLog;
    }

    public String getIdLogUser() {
        return idLogUser;
    }

    public void setIdLogUser(String idLogUser) {
        this.idLogUser = idLogUser;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getIsi() {
        return isi;
    }

    public void setIsi(String isi) {
        this.isi = isi;
    }
}
